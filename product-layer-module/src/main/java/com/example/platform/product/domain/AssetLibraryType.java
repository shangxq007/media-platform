package com.example.platform.product.domain;

/**
 * Types of asset libraries.
 */
public enum AssetLibraryType {
    /**
     * General purpose asset library.
     */
    GENERAL,

    /**
     * Video-specific assets.
     */
    VIDEO,

    /**
     * Audio-specific assets (music, SFX).
     */
    AUDIO,

    /**
     * Image assets (logos, graphics).
     */
    IMAGE,

    /**
     * Template assets (reusable components).
     */
    TEMPLATE,

    /**
     * Brand assets (logos, colors, fonts).
     */
    BRAND
}
